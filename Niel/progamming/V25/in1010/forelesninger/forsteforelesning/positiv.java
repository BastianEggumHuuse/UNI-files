class Positiv {
    public static void main (String[] args) {
        int v = 42;

        if (v > 0) {
            System.out.println(v + " er positiv");
        } else if (v == 0) {
            System.out.println(v + " er null");
        } else {
            System.out.println(v + " er negativ");
        }
    }
}